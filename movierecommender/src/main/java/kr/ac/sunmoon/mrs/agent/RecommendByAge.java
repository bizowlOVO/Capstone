package kr.ac.sunmoon.mrs.agent;

public class RecommendByAge {
	private int recommendSeq;
	private int movieSeq;
	private int child;
	private int youth;
	private int uniStudent;
	private int worker;
	
	public int getRecommendSeq() {
		return recommendSeq;
	}
	
	public void setRecommendSeq(int recommendSeq) {
		this.recommendSeq = recommendSeq;
	}
	
	public int getMovieSeq() {
		return movieSeq;
	}
	
	public void setMovieSeq(int movieSeq) {
		this.movieSeq = movieSeq;
	}
	
	public int getChild() {
		return child;
	}
	
	public void setChild(int child) {
		this.child = child;
	}
	
	public int getYouth() {
		return youth;
	}
	
	public void setYouth(int youth) {
		this.youth = youth;
	}
	
	public int getUniStudent() {
		return uniStudent;
	}
	
	public void setUniStudent(int uniStudent) {
		this.uniStudent = uniStudent;
	}
	
	public int getWorker() {
		return worker;
	}
	
	public void setWorker(int worker) {
		this.worker = worker;
	}
}
